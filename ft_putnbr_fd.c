#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char		c;
	unsigned	nb;

	if (n < 0)
		write(fd, "-", 1);
	nb = (n < 0) ? -n : n;
	if (nb > 9)
		ft_putnbr_fd(nb / 10, fd);
	c = nb % 10 + 48;
	write(fd, &c, 1);
}
